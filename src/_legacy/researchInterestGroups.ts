export function getResearchBulletsForPhase(groupIndex: number, phase: ResearchInterestPhase): string[] {
  const n = RESEARCH_INTEREST_GROUP_COUNT;
  const gi = ((groupIndex % n) + n) % n;
  const group = RESEARCH_INTEREST_GROUPS[gi];
  if (!group) return [];
  return [...group[phase]];
}
